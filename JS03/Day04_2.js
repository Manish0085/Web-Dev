// Looping
// for loop
console.log("Post Increament")
for(let i=0; i<10; i++){
    console.log(i);
}

console.log("Post decrement")
for(let i=10; i>=0; i--){
    console.log(i);
}


// while loop
console.log("while loop")
let i = 0;
while(i < 10){
    console.log(i);
    i++;
}



// while loop
console.log("do-while loop")
let j = 0;
do {
    console.log(j);
    j++;
} while(j < 10);